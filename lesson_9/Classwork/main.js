let nextTask = document.createElement('div')
nextTask.classList.add('nextText')
document.body.append(nextTask)

let blocker = (coursesArray) => {
    for (let elem of coursesArray) {
        let courseBlock = document.createElement('div')
        let block_head = document.createElement('div')
        block_head.classList.add('block_head')
        courseBlock.append(block_head)
        courseBlock.classList.add('courseBlock')
        nextTask.append(courseBlock)

        for (let elemIN in elem) {
            let innerBlock = document.createElement('div')
            courseBlock.append(innerBlock)
            if (Array.isArray(elem[`${elemIN}`])) {
                console.log(elem[`${elemIN}`])
                let modules = document.createElement('ul')
                for (let subElList of elem[`${elemIN}`]) {
                    let modulesEl = document.createElement('li')
                    modulesEl.innerText = subElList
                    modules.append(modulesEl)
                }
                innerBlock.append(modules)
            } else {
                innerBlock.innerText = elem[`${elemIN}`]
                block_head.append(innerBlock)
            }
        }
    }
}
